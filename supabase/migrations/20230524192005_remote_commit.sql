CREATE FUNCTION "public"."match_embeddings"("embedding" "public"."vector", "match_threshold" double precision, "match_count" integer) RETURNS TABLE("content_path" "text", "content" "text", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
#variable_conflict use_variable
begin
  return query
  select
    '/info/' + page.directory + '/' + page.page_name + '#user-content-' + coalesce(page.sub_section, page.section) as content_path,
    page_embedding.text as content,
    (page_embedding.embedding <#> embedding) * -1 as similarity
  from page_embedding JOIN page on page_embedding.page_id = page.id

  -- The dot product is negative because of a Postgres limitation, so we negate it
  where (page_section.embedding <#> embedding) * -1 > match_threshold
  
  order by page_section.embedding <#> embedding

  limit match_count;
end;
$$;

